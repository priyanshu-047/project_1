package javasrc;
 


public class Com_details {
    private String nameString;
    private String locString;
    private int salaryString;
    private int mincgp;
    private String catString;
	public String getNameString() {
		return nameString;
	}
	public void setNameString(String nameString) {
		this.nameString = nameString;
	}
	public String getLocString() {
		return locString;
	}
	public void setLocString(String locString) {
		this.locString = locString;
	}
	public int getSalaryString() {
		return salaryString;
	}
	public void setSalaryString(int salaryString) {
		this.salaryString = salaryString;
	}
	public int getMincgp() {
		return mincgp;
	}
	public void setMincgp(int mincgp) {
		this.mincgp = mincgp;
	}
	public String getCatString() {
		return catString;
	}
	public void setCatString(String catString) {
		this.catString = catString;
	}
	
	
}
